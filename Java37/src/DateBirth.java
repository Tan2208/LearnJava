import java.util.Objects;

public class DateBirth {
    private int day,month,year;

    public DateBirth(int day, int month, int year) {
        this.day = day;
        this.month = month;
        this.year = year;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    @Override
    public boolean equals(Object o) {
        if (o == null || getClass() != o.getClass()) return false;
        DateBirth dateBirth = (DateBirth) o;
        return day == dateBirth.day && month == dateBirth.month && year == dateBirth.year;
    }


}
